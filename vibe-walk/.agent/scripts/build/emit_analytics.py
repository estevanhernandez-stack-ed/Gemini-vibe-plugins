"""
emit_analytics.py — M5 analytics wiring + TOUR_ANALYTICS.md emitter.

Pure function. No I/O. No side effects.

emit_analytics(build_plan: dict) -> dict
  Returns {"files": {"<relpath>": "<contents>", ...}, "warnings": [...]}

Emits two files:

  tourAnalytics.ts
    A host-agnostic analytics adapter. Exposes:
      trackTourEvent(event, payload) — the single seam the host wires to
        their own analytics provider (Amplitude, Mixpanel, Pendo, Segment, etc.).
        The default implementation is a no-op console stub; the host replaces it.
    Wires the 6-event schema to Driver.js substrate callbacks:
      tour_started      → emitted at drive start (inline in startSpotlightTour)
      tour_step_viewed  → onHighlightStarted
      tour_step_advanced → onNextClick
      tour_skipped      → onDestroyStarted when NOT on the last step
      tour_completed    → onDestroyStarted when ON the last step
      tour_replayed     → emitted at replay invocation

  TOUR_ANALYTICS.md
    Names the 6 events, the host's activation event (placeholder), and the
    attribution windows (7-day feature adoption, 14-day retention).
    States plainly that completion is a trap metric; success is downstream
    activation.

Decision constraints honored:
  D5 — 6-event schema wired to substrate hooks; no vendor hardcoded; host-agnostic.
  Celestia3 shipped dark (no analytics) — this emitter ensures that never repeats.

Source: docs/inputs/research/_seed.md §2 (D5), §3 Phase 2, §4 GENERATE list.
"""
from __future__ import annotations


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def emit_analytics(build_plan: dict) -> dict:
    """
    Emit analytics wiring + TOUR_ANALYTICS.md from a resolved build-plan.

    Parameters
    ----------
    build_plan : dict
        The fully resolved .vibe-walk/build-plan.json (as a Python dict).
        Required keys: substrate, ranked_shortlist, aha_moment, app_path.

    Returns
    -------
    dict:
        "files"    — dict of {relative_path: file_contents}
        "warnings" — list of warning strings (empty when clean)
    """
    warnings: list[str] = []

    shortlist = build_plan.get("ranked_shortlist", [])
    step_count = len(shortlist)
    app_path = build_plan.get("app_path", "")

    analytics_ts = _emit_analytics_ts(step_count)
    analytics_md = _emit_analytics_md(app_path)

    return {
        "files": {
            "tourAnalytics.ts": analytics_ts,
            "TOUR_ANALYTICS.md": analytics_md,
        },
        "warnings": warnings,
    }


# ---------------------------------------------------------------------------
# TypeScript analytics adapter emitter
# ---------------------------------------------------------------------------

def _emit_analytics_ts(step_count: int) -> str:
    """
    Emit tourAnalytics.ts — the host-agnostic analytics wiring for Driver.js.

    The host replaces the stub trackTourEvent implementation with their own
    analytics provider call. All six events flow through this single seam.
    """
    return f"""\
// tourAnalytics.ts
// Generated by vibe-walk emit_analytics — edit freely.
//
// WIRING: replace the trackTourEvent stub with your analytics provider call.
// All 6 tour events flow through this single function — wire it once,
// and every event lands in your dashboard.
//
// See TOUR_ANALYTICS.md for the full wiring guide + provider examples.

// ---------------------------------------------------------------------------
// Host-agnostic seam — replace this stub with your analytics provider call.
// ---------------------------------------------------------------------------

/**
 * trackTourEvent — the single analytics seam for the spotlight tour.
 *
 * Replace this stub:
 *
 *   export function trackTourEvent(event: string, payload: Record<string, unknown>): void {{
 *     yourAnalyticsProvider.track(event, payload);
 *   }}
 *
 * event: one of the 6 tour event names (see TOUR_ANALYTICS.md)
 * payload: event-specific properties (step_index, step_name, tour_id, etc.)
 */
export function trackTourEvent(
  event: string,
  payload: Record<string, unknown> = {{}},
): void {{
  // STUB — replace with your analytics provider call.
  // This no-op prevents console noise in production until you wire it.
  if (process.env.NODE_ENV === 'development') {{
    console.debug('[vibe-walk analytics]', event, payload);
  }}
}}

// ---------------------------------------------------------------------------
// Driver.js callback handlers — wire these into your driver() config.
//
// Usage: pass these as callbacks when constructing your driver() instance
// (alongside the SPOTLIGHT_STEPS from spotlightTour.ts).
//
// Example integration in spotlightTour.ts:
//   import {{ onHighlightStarted, onNextClick, onDestroyStarted, emitTourStarted }}
//     from './tourAnalytics';
//
//   const tour = driver({{
//     steps: SPOTLIGHT_STEPS,
//     onHighlightStarted,
//     onNextClick,
//     onDestroyStarted,
//     onDestroyed: onDone,
//   }});
//   emitTourStarted({{ tour_id: 'spotlight' }});
//   tour.drive();
// ---------------------------------------------------------------------------

const TOTAL_STEPS = {step_count};

/**
 * emitTourStarted — call immediately before tour.drive().
 * Event: tour_started
 */
export function emitTourStarted(payload: Record<string, unknown> = {{}}): void {{
  trackTourEvent('tour_started', {{ total_steps: TOTAL_STEPS, ...payload }});
}}

/**
 * onHighlightStarted — Driver.js callback fired when a step becomes active.
 * Event: tour_step_viewed
 *
 * Wire to driver({{ onHighlightStarted }})
 */
export function onHighlightStarted(
  element: Element | undefined,
  step: {{ index: number; [key: string]: unknown }},
  _options: unknown,
): void {{
  trackTourEvent('tour_step_viewed', {{
    step_index: step.index,
    step_name: (step as {{ popover?: {{ title?: string }} }}).popover?.title ?? '',
    total_steps: TOTAL_STEPS,
  }});
}}

/**
 * onNextClick — Driver.js callback fired when the user clicks "Next".
 * Event: tour_step_advanced
 *
 * Wire to driver({{ onNextClick }})
 */
export function onNextClick(
  element: Element | undefined,
  step: {{ index: number; [key: string]: unknown }},
  _options: unknown,
): void {{
  trackTourEvent('tour_step_advanced', {{
    step_index: step.index,
    step_name: (step as {{ popover?: {{ title?: string }} }}).popover?.title ?? '',
    total_steps: TOTAL_STEPS,
  }});
}}

/**
 * onDestroyStarted — Driver.js callback fired when the tour is about to be
 * destroyed (close button, ESC, or natural completion of last step).
 *
 * Distinguishes tour_skipped from tour_completed by checking whether the
 * active step index is the last step.
 *
 *   - On the last step  → tour_completed (user saw every step)
 *   - Before last step  → tour_skipped   (user bailed early)
 *
 * Wire to driver({{ onDestroyStarted }})
 */
export function onDestroyStarted(
  element: Element | undefined,
  step: {{ index: number; [key: string]: unknown }},
  _options: unknown,
): void {{
  const isLastStep = step.index >= TOTAL_STEPS - 1;
  if (isLastStep) {{
    trackTourEvent('tour_completed', {{
      step_index: step.index,
      total_steps: TOTAL_STEPS,
    }});
  }} else {{
    trackTourEvent('tour_skipped', {{
      step_index: step.index,
      skipped_at_step: step.index + 1,
      total_steps: TOTAL_STEPS,
    }});
  }}
}}

/**
 * emitTourReplayed — call at the point the user triggers a replay
 * (e.g., from a help menu or "Take the Tour Again" button).
 * Event: tour_replayed
 *
 * Call this before invoking replaySpotlightTour() from spotlightTour.ts.
 */
export function emitTourReplayed(payload: Record<string, unknown> = {{}}): void {{
  trackTourEvent('tour_replayed', {{ total_steps: TOTAL_STEPS, ...payload }});
}}
"""


# ---------------------------------------------------------------------------
# TOUR_ANALYTICS.md emitter
# ---------------------------------------------------------------------------

def _emit_analytics_md(app_path: str) -> str:
    """
    Emit TOUR_ANALYTICS.md — names events, windows, and the host activation event.
    """
    return """\
# TOUR_ANALYTICS.md
<!-- Generated by vibe-walk emit_analytics — edit to add your activation event. -->

## Why this doc exists

The Celestia3 tour shipped with no analytics — its activation impact was completely
dark. This doc ensures every vibe-walk-generated tour is instrumented from day one.

**Completion is a trap metric.** A tour that gets high completion but drives zero
downstream activation is a waste of users' attention. The real success criterion is
downstream activation: does the user perform the feature's core value action within
7 days? Are they still active at 14 days?

---

## The 6 tour events

Wire `trackTourEvent` in `tourAnalytics.ts` to your analytics provider. All six
events flow through that single function.

| Event | Fires when | Key payload fields |
|---|---|---|
| `tour_started` | User begins the tour (before first step) | `total_steps` |
| `tour_step_viewed` | A step becomes active (`onHighlightStarted`) | `step_index`, `step_name`, `total_steps` |
| `tour_step_advanced` | User clicks "Next" (`onNextClick`) | `step_index`, `step_name`, `total_steps` |
| `tour_skipped` | Tour destroyed before the last step (`onDestroyStarted`, not last step) | `step_index`, `skipped_at_step`, `total_steps` |
| `tour_completed` | Tour destroyed on the last step (`onDestroyStarted`, last step) | `step_index`, `total_steps` |
| `tour_replayed` | User triggers a replay | `total_steps` |

### How `tour_skipped` vs `tour_completed` are distinguished

Both fire via Driver.js's `onDestroyStarted` callback (fired before the tour is
destroyed — covers close button, ESC, and natural last-step completion). The
distinction is a last-step check:

```ts
const isLastStep = step.index >= TOTAL_STEPS - 1;
// isLastStep → tour_completed; else → tour_skipped
```

---

## Your activation event (host fills this in)

Add your own downstream activation event to close the attribution loop.
This is the action that proves the tour worked — not that they watched it,
but that they *did the thing*.

```ts
// Replace YOUR_ACTIVATION_EVENT with your product's core value action.
// Examples:
//   "project_created"    — user created their first project
//   "analysis_run"       — user ran their first analysis
//   "team_member_invited" — user invited a teammate
//   "export_completed"   — user exported a result

trackEvent('YOUR_ACTIVATION_EVENT', { source: 'post_tour' });
```

Capture this event in your product code — not in this file — and attribute it
back to the tour using the windows below.

---

## Attribution windows

These windows define "did the tour work?"

| Window | What it measures | Why |
|---|---|---|
| **7-day feature adoption** | User performs `YOUR_ACTIVATION_EVENT` within 7 days of `tour_completed` or `tour_skipped` | Tight enough to attribute causally; long enough for async workflows |
| **14-day retention** | User is still active 14 days after tour exposure | Tours that improve feature adoption should also improve early retention |

**Do not use `tour_completed` alone as your success metric.** A user who completes
the tour but never activates the feature is a failed tour. A user who skips at step 2
but activates the feature is a success.

---

## Wiring guide

1. Open `tourAnalytics.ts` and replace the `trackTourEvent` stub:

   ```ts
   export function trackTourEvent(event: string, payload: Record<string, unknown>): void {
     // Replace with your provider:
     yourAnalyticsProvider.track(event, payload);
   }
   ```

2. In `spotlightTour.ts`, import and wire the callbacks:

   ```ts
   import {
     emitTourStarted,
     onHighlightStarted,
     onNextClick,
     onDestroyStarted,
     emitTourReplayed,
   } from './tourAnalytics';

   export function startSpotlightTour(onDone?: () => void): void {
     if (typeof window === 'undefined') return;
     emitTourStarted({ tour_id: 'spotlight' });
     const tour = driver({
       steps: SPOTLIGHT_STEPS,
       onHighlightStarted,
       onNextClick,
       onDestroyStarted,
       onDestroyed: onDone,
     });
     tour.drive();
   }

   export const replaySpotlightTour = (): void => {
     emitTourReplayed({ tour_id: 'spotlight' });
     startSpotlightTour();
   };
   ```

3. Create a funnel in your analytics dashboard:
   `tour_started` → `tour_step_viewed` (×N) → `tour_completed` → `YOUR_ACTIVATION_EVENT`

4. Set up the 7d and 14d retention cohorts using `tour_started` as the cohort entry.

---

## References

- Decision D5: `docs/inputs/research/_seed.md` §2
- Driver.js callback docs: https://driverjs.com/docs/configuration
- The Celestia3 dark-data postmortem: the tour shipped on feat/spotlight-tour with
  no analytics wiring — activation impact is unknown. This file exists so that
  never happens again.
"""
